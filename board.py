class Board:
    def __init__(self, game):
        self.game = game
        self.levels = boards
        self.current_level = 0
        self.settings = game.settings
        self.screen = game.screen
        self.tiles_color = "blue" #here we can change the color of the tiles depending on the level
        self.dot_color = (212, 175, 55)
        self.pellet_color = (255, 223, 0)
        self.board_width = 30
        self.board_height = 36
    
    def draw_board(self):
        padding_bottom = 100
        num1 = ((self.settings.screen_height - padding_bottom) // 32)
        num2 = (self.settings.screen_width // 30)
        level = self.levels[self.current_level]
        for i in range(len(level)):
            for j in range(len(level[i])):
                if level[i][j] == 1:
                    pygame.draw.circle(self.screen, self.dot_color, (j * num2 + (0.5 * num2), i * num1 + (0.5 * num1)), 4)
                if level[i][j] == 2: #TODO: add flicker effect here with a bool that can be managed in the run
                    pygame.draw.circle(self.screen, self.pellet_color, (j * num2 + (0.5 * num2), i * num1 + (0.5 * num1)), 10)
                if level[i][j] == 3:
                    pygame.draw.aaline(self.screen, self.tiles_color, (j * num2 + (0.5 * num2), i * num1),
                                    (j * num2 + (0.5 * num2), i * num1 + num1), 3)
                if level[i][j] == 4:
                    pygame.draw.aaline(self.screen, self.tiles_color, (j * num2, i * num1 + (0.5 * num1)),
                                    (j * num2 + num2, i * num1 + (0.5 * num1)), 3)
                if level[i][j] == 5:
                    pygame.draw.arc(self.screen, self.tiles_color, [(j * num2 - (num2 * 0.4)) - 2, (i * num1 + (0.5 * num1)), num2, num1],
                                    0, PI / 2, 1)
                if level[i][j] == 6:
                    pygame.draw.arc(self.screen, self.tiles_color,
                                    [(j * num2 + (num2 * 0.5)), (i * num1 + (0.5 * num1)), num2, num1], PI / 2, PI, 1)
                if level[i][j] == 7:
                    pygame.draw.arc(self.screen, self.tiles_color, [(j * num2 + (num2 * 0.5)), (i * num1 - (0.4 * num1)), num2, num1], PI,
                                    3 * PI / 2, 1)
                if level[i][j] == 8:
                    pygame.draw.arc(self.screen, self.tiles_color,
                                    [(j * num2 - (num2 * 0.4)) - 2, (i * num1 - (0.4 * num1)), num2, num1], 3 * PI / 2,
                                    2 * PI, 1)
                if level[i][j] == 9:
                    pygame.draw.aaline(self.screen, 'white', (j * num2, i * num1 + (0.5 * num1)),
                                    (j * num2 + num2, i * num1 + (0.5 * num1)), 3)
                    
    def update(self):
        self.draw_board()